def expand_phrase_variants(phrase: str) -> set[str]:
    variants = set()
    base = phrase.strip()
    for prefix in QUESTION_PREFIXES:
        for suffix in QUESTION_SUFFIXES:
            candidate = f"{prefix}{base}{suffix}".strip()
            variants.add(candidate)
            variants.add(candidate.capitalize())
    return {item for item in variants if item}


def expand_dialogue_variants(phrase: str) -> set[str]:
    variants = set()
    base = " ".join(phrase.split()).strip(" ,")
    if not base:
        return variants
    variants.add(base)
    variants.add(base.capitalize())
    variants.add(f"{base}?")
    variants.add(f"{base.capitalize()}?")
    for prefix in DIALOGUE_PREFIXES:
        candidate = f"{prefix}{base}".strip()
        variants.add(candidate)
        variants.add(candidate.capitalize())
        for suffix in DIALOGUE_SUFFIXES:
            if not suffix:
                continue
            full = f"{candidate}{suffix}".strip()
            variants.add(full)
            variants.add(full.capitalize())
    return {item for item in variants if item}


def is_natural_dialogue_phrase(text: str) -> bool:
    compact = " ".join(text.split()).strip(" ,")
    if not compact:
        return False
    banned_double = [
        "кстати кстати",
        "слушай слушай",
        "вообще вообще",
        "просто просто",
        "а вообще вообще",
    ]
    lowered = compact.lower()
    if any(chunk in lowered for chunk in banned_double):
        return False
    words = lowered.split()
    repeated_neighbors = sum(1 for idx in range(1, len(words)) if words[idx] == words[idx - 1])
    if repeated_neighbors > 0:
        return False
    return True


def detect_dialogue_topic(question: str, answer: str) -> str:
    combined = f"{question.lower()} {answer.lower()}"
    if any(word in combined for word in {"погода", "дожд", "солнеч", "тепло", "жарко"}):
        return TOPIC_LABELS["weather"]
    if any(word in combined for word in {"грустно", "скучно", "настроение", "без спешки"}):
        return TOPIC_LABELS["emotion"]
    if any(word in combined for word in {"гитара", "клавиш", "укулеле", "рок", "джаз", "музыка", "плейлист"}):
        return TOPIC_LABELS["music"]
    if any(word in combined for word in {"бюджет", "подбор", "подобрать", "инструмент", "оформ", "вариант"}):
        return TOPIC_LABELS["sales"]
    if any(word in combined for word in {"поболтать", "пообщаться", "хобби", "день", "привет", "как дела"}):
        return TOPIC_LABELS["smalltalk"]
    return TOPIC_LABELS["general"]


def generate_large_dialogues() -> list[tuple[str, str]]:
    pairs: list[tuple[str, str]] = []
    for topic, answer in SMALLTALK_TOPICS.items():
        for filler in TOPIC_FILLERS:
            for template in SMALLTALK_TEMPLATES:
                question = template.format(filler=filler, topic=topic).strip()
                for variant in expand_dialogue_variants(question):
                    if is_natural_dialogue_phrase(variant):
                        pairs.append((variant, answer))

    for filler in TOPIC_FILLERS:
        for instrument in INSTRUMENTS:
            for genre in GENRES:
                for template in MUSIC_BRIDGE_TEMPLATES:
                    question = template.format(filler=filler, instrument=instrument, genre=genre).strip()
                    answer = (
                        f"Звучит интересно. Если тебе правда нравится идея с {instrument}, я могу спокойно помочь понять, "
                        f"подойдёт ли такой инструмент под твой вкус, опыт и бюджет."
                    )
                    for variant in expand_dialogue_variants(question):
                        if is_natural_dialogue_phrase(variant):
                            pairs.append((variant, answer))

    for instrument in INSTRUMENTS:
        for genre in GENRES:
            for budget in BUDGETS:
                for purpose in PURPOSES:
                    for experience in EXPERIENCE_PHRASES:
                        question = (
                            f"{experience}, хочу инструмент для {purpose}, думаю о {instrument}, "
                            f"люблю {genre}, бюджет примерно {budget}"
                        )
                        answer = (
                            f"У тебя уже есть неплохие вводные: {instrument}, жанр {genre} и бюджет около {budget} рублей. "
                            f"С такими параметрами уже можно подобрать осмысленный вариант, а не советовать наугад."
                        )
                        pairs.append((question, answer))

    for questions, answer in FUNNEL_DIALOGUE_BLOCKS:
        for question in questions:
            for variant in expand_dialogue_variants(question):
                if is_natural_dialogue_phrase(variant):
                    pairs.append((variant, answer))

    for question, answer in PRECISE_SMALLTALK_DIALOGUES:
        for variant in expand_dialogue_variants(question):
            if is_natural_dialogue_phrase(variant):
                pairs.append((variant, answer))
    return pairs


def expand_examples(seed_examples: list[str]) -> list[str]:
    prefixes = ["", "ну ", "слушай, ", "подскажи, "]
    suffixes = ["", " пожалуйста", " сейчас"]
    generated = set()
    for example in seed_examples:
        base = " ".join(example.split()).strip()
        if not base:
            continue
        generated.add(base)
        generated.add(base.capitalize())
        for prefix in prefixes:
            for suffix in suffixes:
                candidate = f"{prefix}{base}{suffix}".strip()
                generated.add(candidate)
                generated.add(candidate.capitalize())
        if len(base.split()) >= 2 and not any(base.startswith(word) for word in {"привет", "добрый", "пока", "спасибо", "здравствуй", "хай", "хелло"}):
            generated.add(f"мне {base}".strip())
            generated.add(f"я {base}".strip())
    return sorted(item for item in generated if len(item.split()) <= 8)


def build_dataset() -> dict:
    intents = {}
    for intent_name, payload in BASE_INTENTS.items():
        source_examples = list(payload["examples"])
        source_examples.extend(SHORT_REPLIES.get(intent_name, []))
        source_examples.extend(EXTRA_SHORT_INTENTS.get(intent_name, []))
        source_examples.extend(LIVE_PHRASE_GROUPS.get(intent_name, []))
        intents[intent_name] = {
            "examples": expand_examples(source_examples),
            "responses": payload["responses"],
        }

    for ambiguous in {"ага", "ок", "да", "нет", "неа", "интересно"}:
        for intent_name in {"buy_interest", "reject_product", "music_yes", "music_no"}:
            examples = intents.get(intent_name, {}).get("examples", [])
            intents[intent_name]["examples"] = [example for example in examples if example.lower() != ambiguous]
    return {"intents": intents}


